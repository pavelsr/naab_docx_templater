// script for browserify.sh -> bundle.js

global.written_money_obj = function written_money_obj(money_float) {
	
	var writtenNumber = require('written-number');
	writtenNumber.defaults.lang = 'ru';
	
	var int_obj = new Object();
	int_obj.num = Math.trunc( money_float );
	int_obj.padezh = rub2str( int_obj.num );	
	int_obj.written = writtenNumber( int_obj.num );
	
	var dec_obj = new Object();
	dec_obj.num = ( money_float % 1).toFixed(2)*100;
	dec_obj.padezh = kop2str( dec_obj.num );
	if ( dec_obj.num == 0 ) { dec_obj.num = '00' };

	return { "float": money_float, "int": int_obj, "decimals": dec_obj };
	
}